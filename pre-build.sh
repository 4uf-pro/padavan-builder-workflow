#!/bin/bash
set -euo pipefail

echo "========================================"
echo "Предсборочная оптимизация"
echo "========================================"

# Оптимизация языковых файлов
ДИРЕКТОРИЯ_ЯЗЫКОВ="padavan-ng/trunk/user/www/n56u_ribbon_fixed/lang"

if [[ -d "$ДИРЕКТОРИЯ_ЯЗЫКОВ" ]]; then
    echo "Оптимизация языковых файлов..."
    echo "Сохраняем только: EN.js, RU.js, UK.js"
    
    cd "$ДИРЕКТОРИЯ_ЯЗЫКОВ"
    
    # Удаление всех языков кроме необходимых
    for файл in *.js; do
        case "$файл" in
            EN.js|RU.js|UK.js)
                echo "  Сохраняем: $файл"
                ;;
            *)
                echo "  Удаляем: $файл"
                rm -f "$файл"
                ;;
        esac
    done
    
    echo "Оптимизация языковых файлов завершена"
else
    echo "Предупреждение: Директория языков не найдена: $ДИРЕКТОРИЯ_ЯЗЫКОВ"
fi

# Удаление демонстрационных файлов
ДИРЕКТОРИЯ_ДЕМО="padavan-ng/trunk/user/www/n56u_ribbon_fixed/demo"
if [[ -d "$ДИРЕКТОРИЯ_ДЕМО" ]]; then
    echo "Удаление демонстрационных файлов..."
    rm -rf "$ДИРЕКТОРИЯ_ДЕМО"
fi

echo "========================================"
echo "Предсборочная оптимизация завершена"
echo "========================================"